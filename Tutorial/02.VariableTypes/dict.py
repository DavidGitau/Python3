# Dictionaries
dict = {}
dict['one'] = 'This is one'
dict[2] = 'This is two'
tinyDict = {'name':'David', 'code':6734, 'dept':'sales'}

print(dict['one'])          # Prints the value for 'one' key
print(dict[2])              # value for 2 key
print(tinyDict)             # Complete dictionary
print(tinyDict.keys())      # All the keys
print(tinyDict.values())    # Prints all the vlaues