# Strings
str = 'Hello David'

print(str)          # Prints complete string
print(str[0])       # Prints first character of the string
print(str[-3])      # Third last character
print(str[2:5])     # Prints characters starting from third to fifth
print(str[2:])      # Prints string from third character
print(str[-5:])     # From fifth last character
print(str[:-2])     # Prints string up to second last character
print(str * 2)      # Prints string two times
print(str + ' ' + 'TEST') # Prints concatinated string